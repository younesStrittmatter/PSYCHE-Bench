function [Ntrial Nsub] = PlotFunction(DataMatrix,Colors,Yinf,Ysup,Font);


[Ntrial Nsub]=size(DataMatrix);

curve= nanmean(DataMatrix,2);
sem  = nanstd(DataMatrix')'/sqrt(Nsub);

absci=1:Ntrial;

for n=1:Ntrial;
% first bar
bar(n,curve(n),...
    'FaceColor',Colors(n,:),...
    'EdgeColor',Colors(n,:),...
    'BarWidth',0.75,...
    'LineWidth',3);
hold on
errorbar(n,[curve(n)],[sem(n)],...
    'Color',[0 0 0],...
    'LineStyle','none',...
    'LineWidth',3);
end

ylim([Yinf Ysup]);
set(gca,'FontSize',Font,...
    'XLim',[0 Ntrial+1],...
    'XTick',[1:Ntrial],...
    'YTick',[-1:0.1:1],...
    'XTickLabel','');


